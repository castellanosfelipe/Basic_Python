"""Ajuste error lint
"""
import math

# Redondear
print(round(1.3))

# Entrega el valor absoluto (sin positivo o negativo)
print(abs(-77))

# Redondea al numero superior entero mas cercano
print(math.ceil(1.1))

# Redondea al numero inferior entero mas cercano
print(math.floor(1.999))

# Elevar un numero a potencia de otro
print(math.pow(10, 3))

# Raiz cuadrada
print(math.sqrt(9))
